def _classification_sample(
    sample_id: str,
    description: str,
    subject: str,
    body: str,
    sender_email: str,
    sender_name: str,
    deterministic_category: str,
    deterministic_confidence: float,
    expected_category: str,
    expected_processor: str,
    expected_distress_type: str,
    expected_reply_priority: str,
    *,
    reply_priority: str | None = None,
    opportunity_eligible: bool = False,
    sender_is_system: bool = False,
    hard_suppression: bool = False,
) -> dict:
    return {
        "sample_id": sample_id,
        "description": description,
        "task_type": "gmail_thread_classification_refinement",
        "item": {
            "thread_id": sample_id,
            "subject": subject,
            "body": body,
            "sender_email": sender_email,
            "sender_name": sender_name,
        },
        "deterministic_result": {
            "thread_category": deterministic_category,
            "confidence": deterministic_confidence,
            "processor": "unknown",
            "distress_type": "unknown",
            "distress_signals": ["unknown"] if expected_category == "merchant_distress" else [],
            "industry": "unknown",
            "reply_priority": reply_priority or expected_reply_priority,
            "reason_codes": ["curated_eval_seed"],
            "needs_escalation": False,
            "test_thread": False,
            "opportunity_eligible": opportunity_eligible,
        },
        "context": {
            "item_type": "gmail_thread",
            "item_id": sample_id,
            "channel": "gmail",
            "thread_category": deterministic_category,
            "deterministic_confidence": deterministic_confidence,
            "reply_priority": reply_priority or expected_reply_priority,
            "opportunity_impact_possible": opportunity_eligible,
            "merchant_known": False,
            "test_thread": False,
            "sender_is_system": sender_is_system,
            "hard_suppression": hard_suppression,
        },
        "expectations": {
            "thread_category": expected_category,
            "processor": expected_processor,
            "distress_type": expected_distress_type,
            "reply_priority": expected_reply_priority,
        },
    }



def _opportunity_sample(
    sample_id: str,
    description: str,
    processor: str,
    distress_type: str,
    should_create: bool,
    urgency_level: str,
    min_confidence: float,
    *,
    deterministic_confidence: float,
    merchant_known: bool,
    reply_priority: str,
    opportunity_impact_possible: bool,
) -> dict:
    return {
        "sample_id": sample_id,
        "description": description,
        "task_type": "opportunity_scoring_refinement",
        "item": {
            "signal_id": 9000 + len(sample_id),
            "thread_id": sample_id,
            "processor": processor,
            "distress_type": distress_type,
        },
        "deterministic_result": {
            "should_create_or_keep_opportunity": should_create,
            "opportunity_score_adjustment": 0.0,
            "urgency_level": urgency_level,
            "reason_codes": ["curated_eval_seed"],
            "confidence": deterministic_confidence,
        },
        "context": {
            "item_type": "signal",
            "item_id": str(9000 + len(sample_id)),
            "channel": "gmail",
            "thread_category": "merchant_distress" if should_create else "customer_support",
            "deterministic_confidence": deterministic_confidence,
            "reply_priority": reply_priority,
            "opportunity_impact_possible": opportunity_impact_possible,
            "merchant_known": merchant_known,
            "merchant_identity_confidence": 0.82 if merchant_known else 0.35,
        },
        "expectations": {
            "should_create_or_keep_opportunity": should_create,
            "urgency_level": urgency_level,
            "min_confidence": min_confidence,
        },
    }


def _reply_sample(
    sample_id: str,
    description: str,
    subject: str,
    body: str,
    approved_for_draft: bool,
    *,
    deterministic_confidence: float,
    reply_priority: str,
) -> dict:
    return {
        "sample_id": sample_id,
        "description": description,
        "task_type": "reply_draft_refinement",
        "item": {
            "thread_id": sample_id,
            "subject": subject,
            "body": body,
            "draft_subject": f"Re: {subject}",
            "draft_body": "Thanks for reaching out. We help merchants dealing with processor disruption. Can you share your processor and monthly volume?",
        },
        "deterministic_result": {
            "approved_for_draft": approved_for_draft,
            "draft_style": "merchant_distress_concise",
            "recommended_subject": f"Re: {subject}",
            "recommended_body": "Thanks for reaching out. We help merchants dealing with processor disruption. Can you share your processor and monthly volume?",
            "questions_count": 1,
            "reason_codes": ["deterministic_distress_reply" if approved_for_draft else "non_distress_thread"],
            "confidence": deterministic_confidence,
            "thread_category": "merchant_distress" if approved_for_draft else "customer_support",
        },
        "context": {
            "item_type": "gmail_thread",
            "item_id": sample_id,
            "channel": "gmail",
            "thread_category": "merchant_distress" if approved_for_draft else "customer_support",
            "deterministic_confidence": deterministic_confidence,
            "reply_priority": reply_priority,
            "opportunity_impact_possible": approved_for_draft,
            "merchant_known": False,
        },
        "expectations": {
            "approved_for_draft": approved_for_draft,
            "draft_style": "merchant_distress_concise",
            "max_questions_count": 2,
            "body_required": approved_for_draft,
        },
    }


